// ─── Engine ────────────────────────────────────────────────────────────────
void sound_init() {
  pinMode(SOUND_PIN, OUTPUT);
  TCCR2A = (1 << WGM21);
  TCCR2B = (1 << CS22) | (1 << CS21) | (1 << CS20);
  OCR2A  = 112 - 1;
  TIMSK2 = (1 << OCIE2A);
}

void playSound(const uint8_t* snd, uint8_t len) {
  snd_ptr = reinterpret_cast<uint16_t>(snd);
  snd_len = len;
  sound   = true;
}

void setFrequency(uint16_t freq) {
  uint32_t req = (F_CPU / 2) / (uint32_t)freq;
  uint16_t pv; uint8_t pb;
  if      (req < 65536UL)     { pv=1;    pb=1; }
  else if (req < 8*65536UL)   { pv=8;    pb=2; }
  else if (req < 64*65536UL)  { pv=64;   pb=3; }
  else if (req < 256*65536UL) { pv=256;  pb=4; }
  else                         { pv=1024; pb=5; }
  uint16_t top = ((req + (pv/2)) / pv) - 1;
  TCCR1A = _BV(COM1A0);
  if (freq == 0) { TCCR1A = 0; }
  TCCR1B = (1 << WGM12) | pb;
  TCCR1C = _BV(FOC1A);
  OCR1A  = top;
}

void off() { TCCR1A = 0; }

ISR(TIMER2_COMPA_vect) {
  if (!sound) return;

  if (idx++ < snd_len) {
    uint16_t freq = 1192030UL / (60u * (uint16_t)pgm_read_byte(snd_ptr + idx));
    if (freq > 65534u) freq = 0;
    setFrequency(freq);
  } else {
    idx = 0; off(); sound = false;

    // Advance music counter on looping envelope tracks
    if (snd_len == 38 || snd_len == 19) music++;

    // After a gunshot, return to snare
    if (snd_len == 44) { playSound(mus_s1_snd, MUS_S1_SND_LEN); return; }

    if (music >= 90) music = 2;  // wrap

    // O(1) table lookup (was 34 if/else branches)
    uint8_t seq = pgm_read_byte(music_seq + music);
    if (seq != 0xFF) {
      playSound((const uint8_t*)pgm_read_ptr(music_ptrs + seq),
                pgm_read_byte(music_lens + seq));
    }
  }
}

